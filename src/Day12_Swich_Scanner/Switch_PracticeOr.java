package Day12_Swich_Scanner;

public class Switch_PracticeOr {
    public static void main(String[] args) {

        String productName = "iphone";

        switch(productName){

            case "galaxy":
                System.out.println("Samsung");
                break;

            case "iphone" :
                System.out.println("Apple");
                break;

            case "maxbook":
                System.out.println("Apple");
                break;
        }





    }
}
