package Day12_Swich_Scanner;

public class Switch {
    public static void main(String[] args) {
        switch(1){

            case 5 :
                System.out.println("Five");
                break;

            case 1 :
                System.out.println("one");
                break;

            default :
                System.out.println("Invalid Case");
                break;


        }
    }
}
