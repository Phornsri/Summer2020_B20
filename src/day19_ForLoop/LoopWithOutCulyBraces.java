package day19_ForLoop;

public class LoopWithOutCulyBraces {
    public static void main(String[] args) {
        for(int i = 5; i<10; i++){
            //you can repeat as much as you want
            System.out.println("Hello");
            System.out.println("How are you");
        }
        System.out.println("==================");
        for(int i = 1; i <= 5; i++)
            System.out.println("Hello");
        System.out.println("How are you");


    }
}
